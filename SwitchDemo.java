package newprogs;

public class SwitchDemo {
	public static void main(String args[])
	{
		//Switch
	int n=6;
	switch (n)
	{
	case 1:
		System.out.println("one");
		break;
	case 2:
		System.out.println("Two");
		break;
	case 3:
		System.out.println("Three");
		break;
	default:
		System.out.println("OutOfScope:Select from 1 2 3");
	
	}
	
	}

}
